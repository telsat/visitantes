package to;

import java.util.Date;

public class FechasTO {
	
	private String id;
	private Date fechai;
	private Date fechaf;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Date getFechai() {
		return fechai;
	}
	public void setFechai(Date fechai) {
		this.fechai = fechai;
	}
	public Date getFechaf() {
		return fechaf;
	}
	public void setFechaf(Date fechaf) {
		this.fechaf = fechaf;
	}
	
	

}
